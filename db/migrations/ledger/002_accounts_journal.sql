DO $ledgerly$
BEGIN
	IF NOT EXISTS (
		SELECT 1
		FROM pg_type t
		JOIN pg_namespace n ON n.oid = t.typnamespace
		WHERE n.nspname = 'ledger'
			AND t.typname = 'account_type'
	) THEN
		CREATE TYPE ledger.account_type AS ENUM (
			'asset',
			'liability',
			'equity',
			'income',
			'expense'
		);
	END IF;
END
$ledgerly$;

CREATE TABLE IF NOT EXISTS ledger.accounts (
	id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	code text NOT NULL UNIQUE,
	name text NOT NULL,
	type ledger.account_type NOT NULL,
	currency text,
	created_at timestamptz NOT NULL DEFAULT now(),
	CONSTRAINT accounts_code_not_blank CHECK (code = btrim(code) AND code <> ''),
	CONSTRAINT accounts_code_stable CHECK (code = lower(code)),
	CONSTRAINT accounts_name_not_blank CHECK (name = btrim(name) AND name <> ''),
	CONSTRAINT accounts_currency_iso CHECK (currency IS NULL OR currency ~ '^[A-Z]{3}$')
);

COMMENT ON TABLE ledger.accounts IS
	'Chart of accounts. Cash accounts are intentionally not seeded; banking creates real cash accounts through ledger.EnsureAccount.';

CREATE TABLE IF NOT EXISTS ledger.journal_entries (
	id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	date date NOT NULL,
	description text NOT NULL,
	source_module text NOT NULL,
	source_ref text NOT NULL,
	created_at timestamptz NOT NULL DEFAULT now(),
	CONSTRAINT journal_entries_description_not_blank CHECK (description = btrim(description) AND description <> ''),
	CONSTRAINT journal_entries_source_module_not_blank CHECK (source_module = btrim(source_module) AND source_module <> ''),
	CONSTRAINT journal_entries_source_ref_not_blank CHECK (source_ref = btrim(source_ref) AND source_ref <> '')
);

CREATE INDEX IF NOT EXISTS journal_entries_date_id_idx
	ON ledger.journal_entries (date, id);
CREATE INDEX IF NOT EXISTS journal_entries_source_idx
	ON ledger.journal_entries (source_module, source_ref);

CREATE TABLE IF NOT EXISTS ledger.postings (
	id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	entry_id bigint NOT NULL REFERENCES ledger.journal_entries(id) ON DELETE RESTRICT,
	account_code text NOT NULL REFERENCES ledger.accounts(code) ON DELETE RESTRICT,
	amount bigint NOT NULL,
	currency text NOT NULL,
	amount_gbp bigint NOT NULL,
	CONSTRAINT postings_currency_iso CHECK (currency ~ '^[A-Z]{3}$')
);

CREATE INDEX IF NOT EXISTS postings_entry_id_idx
	ON ledger.postings (entry_id, id);
CREATE INDEX IF NOT EXISTS postings_account_code_idx
	ON ledger.postings (account_code, entry_id, id);

