GRANT SELECT, INSERT ON moneyfx.realised_fx TO PUBLIC;
GRANT SELECT, INSERT ON moneyfx.realised_fx TO ledgerly_moneyfx;

REVOKE ALL PRIVILEGES ON SEQUENCE moneyfx.realised_fx_id_seq FROM PUBLIC;
REVOKE ALL PRIVILEGES ON SEQUENCE moneyfx.realised_fx_id_seq FROM ledgerly_moneyfx;
GRANT USAGE, SELECT ON SEQUENCE moneyfx.realised_fx_id_seq TO PUBLIC;
GRANT USAGE, SELECT ON SEQUENCE moneyfx.realised_fx_id_seq TO ledgerly_moneyfx;
